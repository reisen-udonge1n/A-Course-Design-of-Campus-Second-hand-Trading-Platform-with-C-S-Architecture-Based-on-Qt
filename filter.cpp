// 获取 Filter 单例实例的静态方法（Meyer's Singleton）
Filter &Filter::instance()
{
    // C++11 起，局部 static 变量的初始化是线程安全的
    static Filter filter;  // 唯一实例，程序结束时自动销毁
    return filter;         // 返回单例引用
}

// 检查指定文本是否包含敏感词
// 参数 text：用户输入的待检查文本
// 返回值：true 表示包含敏感词，false 表示通过检查
bool Filter::containsSensitiveWords(const QString &text) const
{
    // 将输入文本转为小写，实现大小写不敏感的匹配
    QString lowerText = text.toLower();
    // 遍历敏感词列表中的每一个词
    for (const QString &word : m_sensitiveWords) {
        // 将当前敏感词也转为小写后，检查输入文本是否包含该词
        if (lowerText.contains(word.toLower())) {
            return true;   // 发现敏感词，立即返回 true
        }
    }
    return false;          // 遍历完所有词都未命中，返回 false
}

